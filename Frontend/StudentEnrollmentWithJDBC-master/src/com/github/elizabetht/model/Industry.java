package com.github.elizabetht.model;

public class Industry {
private String industryID;
private String industryName;
private String industryDescripton;
private String lastModifiedUser;
public String getIndustryID() {
	return industryID;
}
public void setIndustryID(String industryID) {
	this.industryID = industryID;
}
public String getIndustryName() {
	return industryName;
}
public void setIndustryName(String industryName) {
	this.industryName = industryName;
}
public String getIndustryDescripton() {
	return industryDescripton;
}
public void setIndustryDescripton(String industryDescripton) {
	this.industryDescripton = industryDescripton;
}
public String getLastModifiedUser() {
	return lastModifiedUser;
}
public void setLastModifiedUser(String lastModifiedUser) {
	this.lastModifiedUser = lastModifiedUser;
}

}
